export const decodedValue = ([colorA, colorB, ...args]) => {
  return COLORS.indexOf(colorA) * 10 + COLORS.indexOf(colorB);
};

export const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
